<h1>Pflegeteam PIA – Helfer mit Herz</h1>

<p>Viele Menschen sind auf Pflege angewiesen, um ihren Alltag gestalten zu können. 
Daher sind Angehörige daran interessierte diese Pflegeleistungen in professionelle
 Hände zu übergeben. Das Pflegeteam PIA bietet einen ambulanten häuslichen 
 Pflegedienst dessen Grundlage Empathie und Vertrauen sind. 
 Der Mensch steht im Mittelpunkt der häuslichen Pflege.</p>


<p><b>Die Schwerpunkte unserer Arbeit sind die Bereiche der</b>
<ul>
    <li>ambulanten Grund- und Behandlungspflege § 132 / 132a SGB V</li>
    <li>hauswirtschaftlichen Versorgung</li>
    <li>Urlaubs- und Verhinderungspflege § 39 SGB XI</li>
    <li>Beratungsgespräche nach § 37a SGB XI</li>
    <li>zusätzlichen Betreuungsleistungen nach § 45b SGB XI</li>
    <li>grundpflegerischen Versorgung § 36 SGB XI</li>
</ul>
<b>Immer an Ihrer Seite – Unsere Fachkräfte</b></p>


<p>Jeder Patient hat individuelle Wünsche und Bedürfnisse. 
Um diesen gerecht zu werden, stehen Ihnen professionelle Fachkräfte zur Verfügung. 
So gewährleistet das Pflegeteam PIA jederzeit eine persönliche und fachgerecht Betreuung. 
Schließlich wissen wir alle – ALTER ist keine Krankheit.</p>
